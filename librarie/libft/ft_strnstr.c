#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t n)
{
	size_t	i;
	size_t	j;

	if (needle[0] == '\0')
	{
		return ((char *)&haystack[0]);
	}
	if (n == 0)
		return (NULL);
	i = 0;
	while (haystack[i] != '\0' && i <= (n - ft_strlen(needle)))
	{
		j = 0;
		while (needle[j] == haystack[i + j])
		{
			if (needle[j + 1] == '\0')
			{
				return ((char *)&haystack[i]);
			}
			j++;
		}
		i++;
	}
	return (0);
}
